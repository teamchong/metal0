# Test dict methods
person = {"name": "Alice", "age": 30, "city": "NYC"}

# Test keys
all_keys = person.keys()
print("Keys length:")
print(len(all_keys))

# Test values
all_values = person.values()
print("Values length:")
print(len(all_values))

# Test that dict still works
print("Dict length:")
print(len(person))
